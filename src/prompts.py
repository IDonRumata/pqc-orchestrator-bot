"""System prompts and prompt builders for every agent.

Two strict rules apply everywhere in this file:

1. Typography: only short hyphens are used, never long dashes.
2. Output formatting for user-facing agents: Telegram HTML only
   (tags <b>, <i>, <code>, <pre>, <blockquote>). Markdown such as
   ``#``, ``##``, ``**``, ``---`` is forbidden because Telegram renders it
   literally.

The current date and the project memory block are injected at CALL TIME through
the prompt builders, never baked into module level constants, because the bot
is a long running process and an import time date would quickly go stale.
"""
from __future__ import annotations

from datetime import date

_MONTHS_RU = [
    "января", "февраля", "марта", "апреля", "мая", "июня",
    "июля", "августа", "сентября", "октября", "ноября", "декабря",
]


def today_ru() -> str:
    """Return today's date as a human readable Russian string."""
    d = date.today()
    return f"{d.day} {_MONTHS_RU[d.month - 1]} {d.year} года"


