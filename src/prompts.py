def build_specialist_user_prompt(user_query: str, context_block: str | None) -> str:
    """Assemble the user message for a specialist agent, with optional RAG context."""
    if context_block:
        return (
            "CONTEXT (выдержки из базы знаний, используй как фактологический фундамент):\n"
            f"{context_block}\n\n"
            "Запрос пользователя:\n"
            f"{user_query}"
        )
    return f"Запрос пользователя:\n{user_query}"


def build_critic_user_prompt(user_query: str, agent_answers: dict[str, str]) -> str:
    """Assemble the user message for the Chief Critic."""
    parts: list[str] = [f"Исходный запрос пользователя:\n{user_query}\n"]
    parts.append("Ответы задействованных агентов:")
    for agent_name, answer in agent_answers.items():
        parts.append(f"\n=== Агент: {agent_name} ===\n{answer}")
    return "\n".join(parts)


def build_news_classifier_prompt(title: str, body: str) -> str:
    """Assemble the user message for the news classifier."""
    trimmed = body.strip()[:6000]
    return f"Заголовок: {title}\n\nТекст:\n{trimmed}"
