# --- Prompt builders --------------------------------------------------------


def build_router_user_prompt(user_query: str) -> str:
    """User message for the router, carries the fresh current date."""
    return f"Сегодняшняя дата: {today_ru()}.\n\nЗапрос пользователя:\n{user_query}"


def build_specialist_user_prompt(
    user_query: str,
    context_block: str | None,
    memory_block: str | None = None,
) -> str:
    """Assemble the user message for a specialist agent.

    Order: current date, project memory, RAG context, then the actual query.
    """
    parts: list[str] = [f"Сегодняшняя дата: {today_ru()}."]
    if memory_block:
        parts.append(
            "ПАМЯТЬ ПРОЕКТА (что основатель уже сделал, решил или обсудил):\n"
            f"{memory_block}"
        )
    if context_block:
        parts.append(
            "CONTEXT (выдержки из базы знаний и свежих новостей, "
            "используй как фактологический фундамент):\n"
            f"{context_block}"
        )
    parts.append(f"Запрос пользователя:\n{user_query}")
    return "\n\n".join(parts)


def build_critic_user_prompt(
    user_query: str,
    agent_answers: dict[str, str],
    memory_block: str | None = None,
) -> str:
    """Assemble the user message for the Chief Critic."""
    parts: list[str] = [f"Сегодняшняя дата: {today_ru()}."]
    if memory_block:
        parts.append(
            "ПАМЯТЬ ПРОЕКТА (что основатель уже сделал, решил или обсудил):\n"
            f"{memory_block}"
        )
    parts.append(f"Исходный запрос пользователя:\n{user_query}")
    parts.append("Ответы задействованных агентов:")
    for agent_name, answer in agent_answers.items():
        parts.append(f"\n=== Агент: {agent_name} ===\n{answer}")
    return "\n".join(parts)


def build_news_classifier_prompt(title: str, body: str) -> str:
    """Assemble the user message for the news classifier."""
    trimmed = body.strip()[:6000]
    return f"Заголовок: {title}\n\nТекст:\n{trimmed}"


def build_memory_extractor_prompt(user_query: str, final_answer: str) -> str:
    """Assemble the user message for the memory extractor."""
    answer = final_answer.strip()[:4000]
    return (
        f"Вопрос основателя:\n{user_query}\n\n"
        f"Итоговый ответ системы:\n{answer}"
    )


def build_news_digest_prompt(items: list[dict[str, str]]) -> str:
    """Assemble the user message for the news digest writer.

    Each item is a dict with category, title and summary keys.
    """
    lines = [f"Сегодняшняя дата: {today_ru()}.", "", "Обновления за цикл мониторинга:"]
    for index, item in enumerate(items, start=1):
        category = item.get("category", "other")
        title = item.get("title", "").strip()
        summary = item.get("summary", "").strip()
        lines.append(f"{index}. [{category}] {title}")
        if summary:
            lines.append(f"   суть: {summary}")
    return "\n".join(lines)
